#include "fillit.h"

int		ft_sqrtsup(int nb)
{
	int		x;

	x = 0;
	if (x < 0)
		return (0);
	while (x < nb / 4 + 2)
	{
		if ((x * x) >= nb)
			return (x);
		x++;
	}
	return (0);
}
